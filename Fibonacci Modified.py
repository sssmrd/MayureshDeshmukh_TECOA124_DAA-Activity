# Complete the fibonacciModified function below.
def fibonacciModified(t1, t2, n):
    arr=[t1,t2]
    while(len(arr)!=n):
        arr.append((arr[len(arr)-1]*arr[len(arr)-1])+arr[len(arr)-2])
    return arr[len(arr)-1]

